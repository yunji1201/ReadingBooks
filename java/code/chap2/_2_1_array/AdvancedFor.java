package core.code.chap2._2_1_array;

public class AdvancedFor {
    public static void main(String[] args) {

        // 향상된 for 문

        String[] strArr = {"java", "android", "c", "javascript"};

        for (String s : strArr) {
            System.out.println(s);
        }

        int[] arr = {1, 2, 3, 4, 5};
        for (int num : arr) {
            System.out.println(num);
        }
    }
}
